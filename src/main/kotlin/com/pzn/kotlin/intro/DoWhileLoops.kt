package com.pzn.kotlin.intro

fun main() {

//    Do While Loops is quiet similar to while loops, but in do while loops the code will be run first and then check the condition. So in Do While loops the code must be run at least once time

    var i = 0

    do {
        println("Perulangan ke $i")
        i++
    } while (i < 10)

}